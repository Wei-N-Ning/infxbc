	cdc.RegisterConcrete(&MsgCreateUserV01{}, "infxbc/CreateUserV01", nil)
	cdc.RegisterConcrete(&MsgUpdateUserV01{}, "infxbc/UpdateUserV01", nil)
	cdc.RegisterConcrete(&MsgDeleteUserV01{}, "infxbc/DeleteUserV01", nil)

